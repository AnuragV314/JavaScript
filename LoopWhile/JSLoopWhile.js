// Loops can execute a block of code as long as a specified condition is true.
/*
Syntax
while (condition) {
  // code block to be executed
}
*/
let i = 1;
while(i<=10){
    console.log(i);
    i++;
}


// The Do While Loop
/*
Syntax
do {
  // code block to be executed
}
while (condition);
*/
let j = 1;
do{
    console.log(j);
    j++;
}while(j<=10);